function setPieChart(labels, data) {
// Pie Chart Example
var ctx = document.getElementById("myPieChart");

var pie_chart_js = {
  type: 'doughnut',
  data: {
    labels: ["Direct", "Referral", "Social"],
    datasets: [{
      data: [55, 30, 15],
      backgroundColor: ['#4e73df', '#1cc88a', '#36b9cc'],
      hoverBackgroundColor: ['#2e59d9', '#17a673', '#2c9faf'],
      hoverBorderColor: "rgba(234, 236, 244, 1)",
    }],
  },
  options: {
    maintainAspectRatio: false,
    tooltips: {
      backgroundColor: "rgb(255,255,255)",
      bodyFontColor: "#858796",
      borderColor: '#dddfeb',
      borderWidth: 1,
      xPadding: 15,
      yPadding: 15,
      displayColors: false,
      caretPadding: 10,
    },
    legend: {
      display: false
    },
    cutoutPercentage: 60,
  },
}

pie_chart_js.data.labels = labels;
pie_chart_js.data.datasets[0].data = data;
pie_chart_js.data.datasets[0].backgroundColor = CSS_COLOR_NAMES;

var myPieChart = new Chart(ctx, pie_chart_js);
}
